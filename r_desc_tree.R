data(iris)
?set.seed()
set.seed(42)
indexes = sample(x=1:150,size=100)
print(indexes)
train = iris[indexes, ]
test = iris[-indexes, ]
install.packages("tree")
model = tree(formula = Species~.  , data=train)
summary(model)
plot(model)
text(model)
library(RColorBrewer)
pallete = brewer.pal(3,"Set2")
plot(x=iris$Petal.Length,y=iris$Petal.Width,pch=19,col=pallete[as.numeric(iris$Species)],main="length vs petal ",xlab="petal length(cm)",ylab="petal width(cm)")
partition.tree(tree=model , label="Species" , add=TRUE)
pred = predict(object=model,newdata=test,type="class")
table(x=pred , y=test$Species)
confusionMatrix(data=pred,reference = test$Species)
